# Purpose: Generate a branded PowerPoint template (.pptx) with Turas styling.
#          Run once to create the template file in assets/.
# Location: modules/hub_app/lib/create_branded_template.R
# Usage:
#   source("modules/hub_app/lib/create_branded_template.R")
#   create_turas_pptx_template()
