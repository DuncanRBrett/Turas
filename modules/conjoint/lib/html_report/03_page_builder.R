# ==============================================================================
# CONJOINT HTML REPORT - PAGE BUILDER
# ==============================================================================
# Assembles complete HTML document with CSS, layout, and JavaScript
# ==============================================================================

#' Build Complete Conjoint HTML Report Page
#'
#' @param html_data Transformed data from 01_data_transformer
#' @param tables List of HTML table strings from 02_table_builder
#' @param charts List of SVG chart strings from 05_chart_builder
#' @param config Configuration
#' @return Complete HTML document string
#' @keywords internal
build_conjoint_page <- function(html_data, tables, charts, config) {

  brand <- config$brand_colour %||% "#323367"
  accent <- config$accent_colour %||% "#CC9900"
  summary <- html_data$summary

  # Build components
  css <- build_conjoint_css(brand, accent)
  meta_tags <- build_conjoint_meta(summary)
  header <- build_conjoint_header(summary, brand)
  nav <- build_conjoint_nav(html_data)
  overview_panel <- build_overview_panel(html_data, tables, charts)
  utilities_panel <- build_utilities_panel(html_data, tables, charts, brand)
  diagnostics_panel <- build_diagnostics_panel(html_data, tables, charts, brand)
  lc_panel <- build_lc_panel(html_data, tables, charts, brand)
  js <- build_conjoint_js()

  # Assemble
  sprintf(
    '<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
%s
<title>%s - Conjoint Analysis Report</title>
<style>%s</style>
</head>
<body>
%s
%s
<div class="cj-panels">
%s
%s
%s
%s
</div>
<footer class="cj-footer">
<p>Generated by TURAS Analytics Platform | The Research LampPost (Pty) Ltd | %s</p>
</footer>
<script>%s</script>
</body>
</html>',
    meta_tags, summary$project_name, css, header, nav,
    overview_panel, utilities_panel, diagnostics_panel, lc_panel,
    summary$generated, js
  )
}


