# ==============================================================================
# CONJOINT HTML REPORT - PAGE BUILDER
# ==============================================================================
# Assembles the complete HTML document with CSS, layout, and JavaScript.
# Matches Turas tabs-level quality: dark gradient header, sidebar navigation,
# insight blocks, pins, exports, about page, help overlay, print CSS.
# All output is a self-contained HTML string — no htmltools dependency.
# ==============================================================================


#' Build Complete Conjoint HTML Report Page
#'
#' Main orchestrator that assembles all components into a single HTML document.
#'
#' @param html_data Transformed data from 01_data_transformer
#' @param tables List of HTML table strings from 02_table_builder
#' @param charts List of SVG chart strings from 05_chart_builder
#' @param config Configuration list
#' @return Complete HTML document string
#' @keywords internal
build_conjoint_page <- function(html_data, tables, charts, config) {

  brand <- config$brand_colour %||% "#323367"
  accent <- config$accent_colour %||% "#CC9900"
  summary <- html_data$summary

  # Build components
  meta_tags    <- build_conjoint_meta(summary, config)
  css          <- build_conjoint_css(brand, accent)
  print_css    <- build_conjoint_print_css()
  header       <- build_conjoint_header(summary, brand, config)
  nav          <- build_report_tab_nav(html_data, config)
  overview     <- build_overview_panel(html_data, tables, charts, config)
  utilities    <- build_utilities_panel(html_data, tables, charts, brand, config)
  diagnostics  <- build_diagnostics_panel(html_data, tables, charts, brand)
  lc_panel     <- build_lc_panel(html_data, tables, charts, brand)
  wtp_panel    <- build_wtp_panel(html_data, tables, charts, brand)
  simulator    <- build_simulator_panel(html_data, brand)
  about        <- build_about_panel(html_data$about, config)
  pinned       <- build_pinned_panel()
  slides       <- build_slides_panel()
  help_overlay <- build_help_overlay()
  js           <- build_conjoint_js()

  # Simulator data JSON (embedded for JS engine)
  sim_json <- if (!is.null(html_data$simulator_data)) {
    simulator_data_to_json(html_data$simulator_data)
  } else "{}"

  sprintf(
    '<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
%s
<title>%s - Conjoint Analysis Report</title>
<style>%s</style>
<style>%s</style>
</head>
<body>
%s
%s
<div class="cj-panels-wrap">
%s
%s
%s
%s
%s
%s
%s
%s
%s
</div>
%s
<footer class="cj-footer">
<p>Generated by TURAS Analytics Platform | The Research LampPost (Pty) Ltd | %s</p>
</footer>
<script type="application/json" id="cj-simulator-data">%s</script>
<script type="application/json" id="pinned-views-data">[]</script>
<script type="application/json" id="slides-data">[]</script>
<script>%s</script>
</body>
</html>',
    meta_tags, .html_esc(summary$project_name), css, print_css,
    header, nav,
    overview, utilities, diagnostics, lc_panel, wtp_panel, simulator, about, pinned, slides,
    help_overlay,
    summary$generated, sim_json, js
  )
}


# ==============================================================================
# META TAGS (Report Hub Integration)
# ==============================================================================

#' @keywords internal
build_conjoint_meta <- function(summary, config = list()) {
  source_file <- config$source_filename %||% summary$project_name
  sprintf(
    '<meta name="turas-report-type" content="conjoint">
<meta name="turas-generated" content="%s">
<meta name="turas-estimation-method" content="%s">
<meta name="turas-respondents" content="%s">
<meta name="turas-attributes" content="%d">
<meta name="turas-source-filename" content="%s">',
    summary$generated,
    summary$estimation_method,
    as.character(summary$n_respondents %||% "N/A"),
    summary$n_attributes,
    .html_esc(source_file)
  )
}


