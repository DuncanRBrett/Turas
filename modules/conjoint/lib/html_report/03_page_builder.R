# ==============================================================================
# UTILITIES PANEL
# ==============================================================================

build_utilities_panel <- function(html_data, tables, charts, brand) {

  attrs <- names(html_data$utilities_by_attr)

  # Attribute navigation buttons
  nav_buttons <- vapply(seq_along(attrs), function(i) {
    active <- if (i == 1) " active" else ""
    sprintf('<button class="cj-attr-btn%s" onclick="switchCjAttr(\'%s\')">%s</button>',
            active, attrs[i], attrs[i])
  }, character(1))

  # Attribute detail sections
  details <- vapply(seq_along(attrs), function(i) {
    active <- if (i == 1) " active" else ""
    attr_name <- attrs[i]
    chart_html <- charts$utility_charts[[attr_name]] %||% ""
    table_html <- tables$utility_tables[[attr_name]] %||% ""

    sprintf(
      '<div class="cj-attr-detail%s" id="attr-%s">
<div class="cj-chart-container">%s</div>
%s
</div>',
      active, attr_name, chart_html, table_html
    )
  }, character(1))

  sprintf(
    '<div class="cj-panel" id="panel-utilities">
<div class="cj-card">
<h2>Part-Worth Utilities by Attribute</h2>
<div class="cj-attr-nav">%s</div>
%s
</div>
</div>',
    paste(nav_buttons, collapse = "\n"),
    paste(details, collapse = "\n")
  )
}


# ==============================================================================
# DIAGNOSTICS PANEL
# ==============================================================================

build_diagnostics_panel <- function(html_data, tables, charts, brand) {

  sections <- character()

  # Model Fit
  sections <- c(sections, sprintf(
    '<div class="cj-card"><h2>Model Fit</h2>%s</div>',
    tables$model_fit %||% '<p>No model fit data available.</p>'
  ))

  # HB Convergence (if applicable)
  if (!is.null(html_data$hb_data)) {
    hb <- html_data$hb_data
    status_class <- if (isTRUE(hb$convergence$converged)) "cj-status-pass" else "cj-status-fail"
    status_text <- if (isTRUE(hb$convergence$converged)) "CONVERGED" else "NOT CONVERGED"

    convergence_html <- sprintf(
      '<div class="cj-card"><h2>HB Convergence</h2>
<p>Status: <span class="%s">%s</span></p>
<p>Iterations: %s | Burn-in: %s | Draws retained: %s</p>
%s
</div>',
      status_class, status_text,
      format(hb$iterations %||% 0, big.mark = ","),
      format(hb$burnin %||% 0, big.mark = ","),
      format(hb$n_draws %||% 0, big.mark = ","),
      tables$convergence %||% ""
    )
    sections <- c(sections, convergence_html)

    # Respondent quality
    if (!is.null(hb$quality)) {
      quality_html <- sprintf(
        '<div class="cj-card"><h2>Respondent Quality</h2>
<p>Mean RLH: <strong>%.3f</strong> | Chance: %.3f | Flagged: %d respondents</p>
</div>',
        hb$quality$mean_rlh, hb$quality$chance_rlh, hb$quality$n_flagged
      )
      sections <- c(sections, quality_html)
    }
  }

  sprintf(
    '<div class="cj-panel" id="panel-diagnostics">%s</div>',
    paste(sections, collapse = "\n")
  )
}


# ==============================================================================
# LATENT CLASS PANEL
# ==============================================================================

build_lc_panel <- function(html_data, tables, charts, brand) {

  if (is.null(html_data$lc_data)) return('<div class="cj-panel" id="panel-latentclass"></div>')

  lc <- html_data$lc_data
  sections <- character()

  # Summary
  sections <- c(sections, sprintf(
    '<div class="cj-card"><h2>Latent Class Summary</h2>
<div class="cj-kpi-row">
<div class="cj-kpi"><div class="cj-kpi-value">%d</div><div class="cj-kpi-label">Optimal Classes</div></div>
<div class="cj-kpi"><div class="cj-kpi-value">%.3f</div><div class="cj-kpi-label">Entropy R\u00b2</div></div>
</div>
</div>',
    lc$optimal_k, lc$entropy_r2
  ))

  # BIC chart + comparison table
  sections <- c(sections, sprintf(
    '<div class="cj-card"><h2>Model Comparison</h2>
<div class="cj-chart-container">%s</div>
%s
</div>',
    charts$bic %||% "",
    tables$lc_comparison %||% ""
  ))

  # Class sizes
  if (!is.null(lc$class_sizes)) {
    size_rows <- vapply(seq_along(lc$class_sizes), function(c) {
      sprintf('<tr><td>Class %d</td><td class="cj-num">%d</td><td class="cj-num">%.1f%%</td></tr>',
              c, lc$class_sizes[c], lc$class_proportions[c] * 100)
    }, character(1))

    sections <- c(sections, sprintf(
      '<div class="cj-card"><h2>Class Sizes</h2>
<table class="cj-table"><thead><tr><th>Class</th><th>Size</th><th>Percentage</th></tr></thead><tbody>%s</tbody></table>
</div>',
      paste(size_rows, collapse = "\n")
    ))
  }

  sprintf('<div class="cj-panel" id="panel-latentclass">%s</div>', paste(sections, collapse = "\n"))
}


# ==============================================================================
# JAVASCRIPT
# ==============================================================================

build_conjoint_js <- function() {
  # Read from external JS file if available, otherwise inline
  js_path <- file.path(dirname(sys.frame(1)$ofile %||% "."), "js", "conjoint_navigation.js")
  if (file.exists(js_path)) {
    return(paste(readLines(js_path, warn = FALSE), collapse = "\n"))
  }

  # Inline fallback
  '
function switchCjTab(tabName) {
  document.querySelectorAll(".cj-tab").forEach(function(t) { t.classList.remove("active"); });
  document.querySelectorAll(".cj-panel").forEach(function(p) { p.classList.remove("active"); });
  var btn = document.querySelector(".cj-tab[data-tab=\\"" + tabName + "\\"]");
  if (btn) btn.classList.add("active");
  var panel = document.getElementById("panel-" + tabName);
  if (panel) panel.classList.add("active");
}
function switchCjAttr(attrName) {
  document.querySelectorAll(".cj-attr-btn").forEach(function(b) { b.classList.remove("active"); });
  document.querySelectorAll(".cj-attr-detail").forEach(function(d) { d.classList.remove("active"); });
  event.target.classList.add("active");
  var detail = document.getElementById("attr-" + attrName);
  if (detail) detail.classList.add("active");
}
document.addEventListener("DOMContentLoaded", function() { switchCjTab("overview"); });
'
}
