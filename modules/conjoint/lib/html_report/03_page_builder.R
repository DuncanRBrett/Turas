#' Concatenate all JS files inline
#' @keywords internal
build_conjoint_js <- function() {
  js_files <- c(
    "conjoint_navigation.js",
    "simulator_engine.js",
    "simulator_ui.js",
    "simulator_charts.js",
    "conjoint_export.js",
    "conjoint_pins.js",
    "conjoint_charts.js"
  )

  js_dir <- .get_conjoint_js_dir()

  js_parts <- vapply(js_files, function(f) {
    path <- file.path(js_dir, f)
    if (file.exists(path)) {
      paste(readLines(path, warn = FALSE), collapse = "\n")
    } else {
      sprintf("/* %s not found in %s */", f, js_dir)
    }
  }, character(1))

  paste(js_parts, collapse = "\n\n")
}


#' Resolve JS directory path
#' @keywords internal
.get_conjoint_js_dir <- function() {
  # Preferred: use the global set by 99_html_report_main.R
  if (exists(".conjoint_lib_dir", envir = globalenv())) {
    return(file.path(get(".conjoint_lib_dir", envir = globalenv()), "html_report", "js"))
  }
  # Fallback: sys.frame(1)$ofile (fragile)
  ofile <- tryCatch(sys.frame(1)$ofile, error = function(e) NULL)
  if (!is.null(ofile) && nzchar(ofile %||% "")) {
    return(file.path(dirname(ofile), "js"))
  }
  # Last resort
  warning("Cannot determine JS directory: set .conjoint_lib_dir before sourcing.")
  file.path(".", "js")
}


