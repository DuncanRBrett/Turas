# ==============================================================================
# HEADER
# ==============================================================================

build_conjoint_header <- function(summary, brand) {

  badges <- character()
  badges <- c(badges, sprintf('<span class="cj-badge">%s</span>', toupper(summary$estimation_method)))
  if (!is.na(summary$n_respondents)) {
    badges <- c(badges, sprintf('<span class="cj-badge">n=%s</span>', format(summary$n_respondents, big.mark = ",")))
  }
  badges <- c(badges, sprintf('<span class="cj-badge">%d Attributes</span>', summary$n_attributes))
  if (!is.na(summary$converged)) {
    status <- if (summary$converged) "Converged" else "Not Converged"
    badges <- c(badges, sprintf('<span class="cj-badge">%s</span>', status))
  }

  sprintf(
    '<header class="cj-header">
<h1>%s</h1>
<div class="cj-subtitle">Conjoint Analysis Report | Generated %s</div>
<div class="cj-badges">%s</div>
</header>',
    summary$project_name, summary$generated, paste(badges, collapse = "\n")
  )
}


# ==============================================================================
# NAVIGATION
# ==============================================================================

build_conjoint_nav <- function(html_data) {

  tabs <- list(
    list(id = "overview", label = "Overview"),
    list(id = "utilities", label = "Utilities"),
    list(id = "diagnostics", label = "Diagnostics")
  )

  if (!is.null(html_data$lc_data)) {
    tabs <- c(tabs, list(list(id = "latentclass", label = "Latent Classes")))
  }

  buttons <- vapply(tabs, function(t) {
    active <- if (t$id == "overview") ' active' else ""
    sprintf('<button class="cj-tab%s" onclick="switchCjTab(\'%s\')" data-tab="%s">%s</button>',
            active, t$id, t$id, t$label)
  }, character(1))

  sprintf('<nav class="cj-nav">%s</nav>', paste(buttons, collapse = "\n"))
}


